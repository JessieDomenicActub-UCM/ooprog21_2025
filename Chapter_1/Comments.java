/**
 * This is a Javadoc comment.
 * It is used for documenting classes, methods, and variables.
 * Javadoc comments are typically used to generate HTML documentation.
 */
public class Comments {

    // This is a single-line comment.
    // It is used to add short, explanatory notes to the code.
    public static void main(String[] args) {
        
        /*
         * This is a multi-line comment.
         * It can span multiple lines and is useful for
         * explaining a block of code or temporarily disabling it.
         */
        System.out.println("This program demonstrates all three types of comments in Java.");
        System.out.println("Single-line, multi-line, and Javadoc.");

    }
}